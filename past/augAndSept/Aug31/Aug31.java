import java.util.*;

public class Aug31 {
  public static void main (String[] args) {
    int i = 1;
    while (i <= 5) {
      System.out.println("Christy has a doppelganger.");
      i++;
    }
    // for (variable with value; condition ; iterating through )
    for (int j = 1; j <= 5; j++) {
      System.out.println("Alena wants to go to Rice University");
    }
    for (int k = 2; k <= 100; k=k+2) {
      System.out.print(k + " ");
    }
  }
}
